    private void preencherFormulario() {

        txtNome.setText(usuario.getNome());
        txtCPF.setText(usuario.getCpf());

    }

    private void preencherTabelaCaixa() {

        DefaultTableModel modelo = (DefaultTableModel) tabelaCaixa.getModel();

        for (int linha = modelo.getRowCount() - 1; linha >= 0; linha--) {
            modelo.removeRow(linha);
        }

        for (Caixa cx : listaCaixa) {
            modelo.addRow(new String[]{
                formatar.format(cx.getDataHoraAbertura()),
                (cx.getDataHoraFechamento() == null) ? "-" : formatar.format(cx.getDataHoraFechamento()),
                (cx.isAberto() ? "Aberto" : "Fechado")});
        }

    }

