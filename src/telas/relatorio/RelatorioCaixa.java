    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        if (tabelaCaixa.getSelectedRow() < 0) {
            Mensagens.atenção("Selecione uma linha.");
        } else {
            try {
                ImprimirRelatorio.fechamentoCaixa(listaCaixa.get(tabelaCaixa.getSelectedRow()).getCodigo());
            } catch (JRException ex) {
                Mensagens.erro("Erro ao tentar Gerar o Relatorio de Caixa: " + ex.getMessage());
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed


