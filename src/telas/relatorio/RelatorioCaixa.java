    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (!usuarioPrincipal.isAdm()) {
            Mensagens.atenção("Você não tem permissão para visualizar o Caixa de outro Usuário.");
        } else {

            SelecaoUsuario dialogo = new SelecaoUsuario(null, true);

            dialogo.setVisible(true);

            if (dialogo.getUsuario() == null) {

                this.usuario = usuarioPrincipal;

                preencherFormulario();

            } else {
                this.usuario = dialogo.getUsuario();

                preencherFormulario();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if (txtDataInicial.getDate() == null) {
            Mensagens.atenção("O campo Data Inicial não pode ser vazio.");
        } else if (txtDataFinal.getDate() == null) {
            Mensagens.atenção("O campo Data Final não pode ser vazio.");
        } else if (txtDataFinal.getDate().getTime() < txtDataInicial.getDate().getTime()) {
            Mensagens.atenção("A Data Final não pode ser menor que a Data Inicial!");
        } else {
            try {
                listaCaixa = caixaDao.listarPorPeriodo(usuario.getCpf(), txtDataInicial.getDate(), txtDataFinal.getDate());

                if (listaCaixa.isEmpty()) {
                    Mensagens.atenção("Pesquisa não retornou resultados.");

                }

                preencherTabelaCaixa();

            } catch (SQLException ex) {
                Mensagens.erro("Erro ao tentar Lista os Caixas do Usuário: " + usuario.getNome() + "\n Erro: " + ex.getMessage());
            }
        }


    }//GEN-LAST:event_jButton2ActionPerformed

