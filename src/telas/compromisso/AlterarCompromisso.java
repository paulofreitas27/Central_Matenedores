    private void popularEntidade() {

        this.compromisso.setDiaVencimento(txtDataVencimento.getDate());
        this.compromisso.setValor_parcela(Double.parseDouble(txtValor.getText().trim().replace(",", ".")));

        try {

            this.compromisso.setListaBoletos(boDao.listarTodosCompromisso(compromisso.getCodigo()));

            calendario.setTime(compromisso.getDiaVencimento());

            for (Boleto bo : compromisso.getListaBoletos()) {

                if (!bo.isQuitado()) {

                    bo.setValor(compromisso.getValor_parcela());

                }

                if (bo.getNumeroParcela() != 1) {
                    calendario.add(Calendar.MONTH, 1);
                }

                bo.setDataVencimento(calendario.getTime());

            }

        } catch (SQLException ex) {
            Mensagens.erro("Erro ao tentar preencher Compromisso e Boletos." + ex.getMessage());
        }

    }

