    private void preencherFormularioMembro() {

        txtNome.setText(this.compromisso.getMembro().getNome());
        txtFone.setText(this.compromisso.getMembro().getFone());

        jcProjeto.getModel().setSelectedItem(compromisso.getProjeto());
        jcProjeto.setEnabled(false);

        System.out.println("Linha: " + jcProjeto.getSelectedIndex());

        txtNumPasrcelas.getModel().setValue(compromisso.getTotalNumParcela());
        txtNumPasrcelas.setEnabled(false);

        txtValor.setText(String.valueOf(compromisso.getValor_parcela()).replace(".", ","));

        txtDataVencimento.setDate(compromisso.getDiaVencimento());

    }

    private void preecherjComboProjeto() {

        DefaultComboBoxModel modelCidade = (DefaultComboBoxModel) jcProjeto.getModel();

        modelCidade.removeAllElements();

        modelCidade.addElement("Selecione um projeto");

        try {
            for (Projeto pj : projDao.listarTodosAtivos()) {
                modelCidade.addElement(pj);
            }
        } catch (SQLException ex) {
            Mensagens.erro("Problema ao preencher o Lista de Projetos: " + ex.getMessage());
        }
    }

